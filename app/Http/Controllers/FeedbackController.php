     */
    public function store(Request $request)
    {
        //
        $data = new Feedback;
        $data->name = $request->get('name');
        $data->email = $request->get('email');
        $data->telp = $request->get('number');
        $data->note = $request->get('message');
        $data->save();
        return view('content.main.contact');
    }

